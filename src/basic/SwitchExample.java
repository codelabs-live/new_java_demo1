package basic;
public class SwitchExample {
    public static void main(String[] args) {
        int a  =3;
        switch (a){
            case 6:
                System.out.println(a);
                break;
            case 10:
                System.out.println(a);
                break;
            default:
                System.out.println("Error");
        }
    }
}
